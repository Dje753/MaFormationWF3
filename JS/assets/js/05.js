/*----------------------------------
    INCREMENTATION & DECREMENTATION
------------------------------------*/

// ############### INCREMENTATION ############### //

var nb1 = 1;
nb1 = nb1 + 1;

// -- Affichage dans ma console.
console.log(nb1);

// -- Ecriture simplifié
nb1++; // Raccourci de nb1 + 1; -> de 1 en 1

// -- Affichage dans ma console.
console.log(nb1);


// ############### DECREMENTATION ############### //

nb1 = nb1 - 1;

// -- Affichage dans ma console.
console.log(nb1);

// -- Ecriture simplifié
nb1--; // Raccourci de nb1 - 1; -> de 1 en 1

// -- Affichage dans ma console.
console.log(nb1);


// ############### SUBTILITE ############### //

nb1 = 1;
console.log("nb1++ = "+ nb1++);

nb1 = 1;
console.log("++nb1 = "+ ++nb1);